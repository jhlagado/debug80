import type { EncodedZ80Instruction, Z80CoreMnemonic } from './instruction.js';

const CORE_OPCODES: Readonly<Record<Z80CoreMnemonic, readonly number[]>> = {
  di: [0xf3],
  ei: [0xfb],
  scf: [0x37],
  ccf: [0x3f],
  cpl: [0x2f],
  daa: [0x27],
  exx: [0xd9],
  halt: [0x76],
  rlca: [0x07],
  rrca: [0x0f],
  rla: [0x17],
  rra: [0x1f],
  neg: [0xed, 0x44],
  rrd: [0xed, 0x67],
  rld: [0xed, 0x6f],
  ldi: [0xed, 0xa0],
  ldir: [0xed, 0xb0],
  ldd: [0xed, 0xa8],
  lddr: [0xed, 0xb8],
  cpi: [0xed, 0xa1],
  cpir: [0xed, 0xb1],
  cpd: [0xed, 0xa9],
  cpdr: [0xed, 0xb9],
  ini: [0xed, 0xa2],
  inir: [0xed, 0xb2],
  ind: [0xed, 0xaa],
  indr: [0xed, 0xba],
  outi: [0xed, 0xa3],
  otir: [0xed, 0xb3],
  outd: [0xed, 0xab],
  otdr: [0xed, 0xbb],
  reti: [0xed, 0x4d],
  retn: [0xed, 0x45],
};

export function encodeCore(mnemonic: Z80CoreMnemonic): EncodedZ80Instruction {
  const opcode = CORE_OPCODES[mnemonic];
  return {
    size: opcode.length,
    fragments: [{ kind: 'bytes', bytes: opcode }],
  };
}
